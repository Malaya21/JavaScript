const month = 'January';

switch(month){
    case 'January':
        console.log('The entered month is January');
        break;
    case 'February':
        console.log('The entered month is February');
        break;
    case 'March':   
        console.log('The entered month is March');
        
    default:    
        console.log('The entered month is not January, February or March');
        
}
